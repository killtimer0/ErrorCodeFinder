static LPTSTR nt_code2msg(DWORD dwCode)
{
    return MsgFromSys(FORMAT_MESSAGE_FROM_HMODULE, hModNt, dwCode);
}

static void nt_freemsg(LPTSTR lpMessage)
{
    LocalFree(lpMessage);
}

void EcRegNt(ErrorCodeSet *pecs)
{
    pecs->lpName = TEXT("NTSTATUS");
    pecs->pfnMessageFromCode = nt_code2msg;
    pecs->pfnFreeMessage = nt_freemsg;
    pecs->items = ec_nt_items;
    pecs->dwCodeCount = ARRAYSIZE(ec_nt_items);
}

void EcUnregNt()
{
    /* Ignore */
}

