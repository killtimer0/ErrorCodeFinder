static LPTSTR com_code2msg(DWORD dwCode)
{
    DWORD fac = HIWORD(dwCode), code = LOWORD(dwCode);
    if (fac == 0x8007 && code >= 12000)
    {
        return MsgFromSys(FORMAT_MESSAGE_FROM_HMODULE, hModHttp, dwCode & 0xFFFF);
    }
    else
    {
        return MsgFromSys(FORMAT_MESSAGE_FROM_SYSTEM, NULL, dwCode);
    }
}

static void com_freemsg(LPTSTR lpMessage)
{
    LocalFree(lpMessage);
}

void EcRegCom(ErrorCodeSet *pecs)
{
    pecs->lpName = TEXT("HRESULT");
    pecs->pfnMessageFromCode = com_code2msg;
    pecs->pfnFreeMessage = com_freemsg;
    pecs->items = ec_com_items;
    pecs->dwCodeCount = ARRAYSIZE(ec_com_items);
}

void EcUnregCom()
{
    /* Ignore */
}

